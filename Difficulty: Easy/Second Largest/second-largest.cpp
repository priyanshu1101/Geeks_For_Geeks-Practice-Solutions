class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        int maxElem = INT_MIN;
        for(int num:arr) maxElem = max(maxElem,num);
        int secondMaxElem=-1;
        for(int num:arr){
            if(num>secondMaxElem && num<maxElem){
                secondMaxElem=num;
            }
        }
        return secondMaxElem;
    }
};

