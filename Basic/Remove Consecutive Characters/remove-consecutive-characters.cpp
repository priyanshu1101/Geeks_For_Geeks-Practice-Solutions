class Solution{
    public:
    string removeConsecutiveCharacter(string S)
    {
        string ans="";
        char ch='#';
        for(char temp:S){
            if(ch!=temp){
                ans+=temp;
                ch=temp;
            }
        }
        return ans;
    }
};

